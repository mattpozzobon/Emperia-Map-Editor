static inline void emperiaMiniMapColorToRGB(uint8_t color, uint8_t& r, uint8_t& g, uint8_t& b)
{
	if(color < 216) {
		r = EMPERIA_MINIMAP_COLORS[color][0];
		g = EMPERIA_MINIMAP_COLORS[color][1];
		b = EMPERIA_MINIMAP_COLORS[color][2];
	} else {
		r = g = b = 0;
	}
}

